<p1>The adaptive landscape is a concept in evolutionary biology.  Depending on who you ask, it's a metaphor, a model, a useful concept, or a misleading mess.  I like to think of it in a really broad sense, as a mapping between a space of possibilities and the fitness of each possibility.  More specifically, it's often thought of as a multidimensional space in which characteristics of some organism are laid out on the axes, with an additional axis for fitness, and a surface that tells you what the fitness of each combination of characteristics.  The metaphor is between this surface and the surface of an actual landscape, where altitude would represent fitness.  If all of this is sounding like gobbledygook, hopefully this site can help a bit.  There are two ways to use the site - you can either read through the drivel I've written like a book, or you can poke around on your own.  If you'd like to take the full ride, start with this Douglas-Hofstader-inspired dialogue about the adaptive landscape: <a href="./topossibilogy">TOPOSSIBILOGY</a>.
