<h1>TOPOSSIBILOGY</h1>
<br />

<p1>***  If this post / story seems completely odd, I would suggest picking up Douglas Hofstadter's <span style="text-decoration: underline;">Godel, Escher, Bach</span> - from which I steal this style and several characters.  Hofstadter's imaginative use of metaphor and analogy is an inspiration for this project.  It's an extremely fun (and I think very useful) way to think about scientific concepts. ***</p1>
<br /><br /><br />

<p2>
<i>Achilles and Ms. Tortoise have just finished their run, but, being fitness enthusiasts, they decide to follow up by climbing the nearby Mt. Fuji.</i>
<br /><br />
<i>Achilles: </i>A beautiful peak huh? Want to race to the top? I'll give you a head start.
<br /><br />
<i>Tortoise: </i>Ah, I think we've been through this already Achilles, let's just walk.
<br /><br />
<i>Achilles: </i> Of course, of course, I was only joking.
<br /><br />
<i>Tortoise: </i>Hey I brought something I'd been meaning to give you for a little while.
<br /><br />
<i>Tortoise reaches into her bag and brings out an old hardbound green book.</i>
<br /><br />
<i>Tortoise</i>: It's just an old book from the future- kind of a mash-up of a variety of topics. But the author manages to bring them all together into a sort of metaphorical fugue on minds and machines in the spirit of Lewis Carroll.
<br /><br />
<i>Achilles: </i>That sounds like something I could be in - I mean get into.
<br /><br />
<i>Tortoise: </i>Here, you can borrow it, I've spent enough time with my head in this book anyways.
<br /><br />
<i>Achilles: </i>Fantastic! I need something new to read - lately I've been reading a lot about evolutionary biology and a concept called the adaptive landscape, but I need a change.
<br /><br />
<i>Tortoise: </i>Evolutionary biology? And how did a great warrior like yourself get into that?
<br /><br />
<i>Achilles: </i>Well I was running the other day -
<br /><br />
<i>Tortoise: </i>Quite fast I'm sure -
<br /><br />
<i>Achilles: </i> When I stumbled into a lecture a few years in the future, 1932 I think. It was a talk from the great evolutionary biologist Sewall Wright. He was the one who proposed this notion of the adaptive landscape.
<br /><br />
<i>Tortoise: </i>And what did Mr. Wright expound on?
<br /><br />
<i>Achilles: </i>That's part of what I'm trying to wrap my head around right now. He presented a kind of topographical map describing how evolution happens.
<br /><br />
<i>Tortoise: </i> A topographical map? Like the one I have in this bag that shows the contours of Mt. Fuji?
<br /><br />
<i>Achilles: </i> I suppose - but in Sewell's metaphor fitness - by which he means an animal's ability to survive and reproduce - is represented by the altitude.  And the north-south and east-west axes represent some combinations of <i>genes</i> of the animal that can be changed. In some of the papers I've been reading lately, though, the axes are <i>traits</i> of animals instead, which makes a bit more sense to me.
<br /><br />

<img class="figure" src="../images/Wright1.png" alt="Wright1" width="613" height="461" />

<br /><br />
<i>Tortoise: </i>And just what do you mean by traits?
<br /><br />
<i>Achilles: </i>Well they could be anything really, let's say east-west is tail length and north-south is body size. And then you can imagine a population of white-tailed deer on this hillside (<i>gesturing out to a nearby group of grassy hills</i>). But the deer all stand frozen still, and each of them have their body size and tail length completely determined by where they stand. Since fitness is determined by elevation, Natural Selection will move the population up the slope to the peak of the, toward the most fit <i>phenotype</i>, or set of traits.
<br /><br />
<i>Tortoise: </i>So we would be joined at the summit by a herd of identical frozen metaphorical deer? Is that what Mr. Wright said?
<br /><br />
<i>Achilles: </i>Well not exactly. I wasn't really sure if he was talking about a population, like that herd of deer, moving around the landscape, or just a single individual moving around, or the population mean. It all gets quite sticky in the details. He also showed this diagram of these <i>allele combinations</i>, which I really don't know what to make of.
<br /><br />

<img class="figure" src="../images/Wright2.png" alt="Wright2" width="678" height="414" />

<br /><br />
<i>Tortoise: </i>Well perhaps you'd be less confused if you had already read this book I'm lending you. In it, one particularly bright young reptile and her nice, if somewhat slow-witted, friend discuss the central dogma of biology through an analogy with Zen koans.
<br /><br />
<i>Achilles: </i>That sounds splendidly elaborate! But I'm not sure I catch the connection to biology - what is its central dogma?
<br /><br />
<i>Tortoise: </i>I think I'll leave the full explanation to the book. For now, I'll just say that each of us has, at our core, a set of <i>genes, </i>and through a few processes these <i>genes </i>make <i>proteins</i>, which carry out functions in our cells. Oh, look - who is that?
<br /><br />
<i>Achilles and Tortoise have made it nearly half the way up Mt. Fuji, and there in front of them is another mountain-climber, an old Crab with a phenomenally large and beautiful beard.</i>
<br /><br />
<i>Crab: </i>Ah hello, hello, hello, goodday to you sir, and madam.
<br /><br />
<i>Achilles &amp; Tortoise: </i>And a good day to you, sir!
<br /><br />
<i>At this point, the author of this dialogue tried removing the comment about Mr. Crab's beard. This removal had no effect on the overall dialogue thus far - it didn't make it any better or worse - so the author kept the beard comment in.</i>
<br /><br />
<i>Crab: </i>Oh thank you! Wondrous mountain isn't it? I've been running around here all weekend, up-and-down, up-and-down, across-and-over, exploring everywhere. I didn't used to wander so much, but I've turned over a new leaf. It's a little nerve-wracking, but quite fun to explore all the possibilities in this amazing place!
<br /><br />
<i>Achilles: </i>It is a magnificent peak. But I can't say I understand any route other than walking up to the top - that's where you can get the most spectacular view.
<br /><br />
<i>Tortoise: </i>Don't be rude Achilles, Mr. Crab is just surveying the whole landscape. Besides, how do you know that there is not a better view elsewhere in the area?
<br /><br />
<i>Achilles: </i>Because I am confident that the highest point will have the best view. That's an assumption I'm quite comfortable with.
<br /><br />
<i>Tortoise: </i>And are you sure the peak you see is the <i>highest point</i>?
<br /><br />
<i>Achilles: </i>Of course! It's silly to suggest otherwise.
<br /><br />
<i>Tortoise: </i>Well I apologize Mr. Crab. If you'd like to hike with us up to the peak, you're welcome to - although you'll have to permit us to continue a fascinating discussion we were having about evolution and biology. Mr. Achilles was recently at a talk by a man named Sewell Wright-
<br /><br />
<i>Crab: </i>Oh excellent - I think I was at the same future-talk as Mr. Achilles, so I'd love to wander with you up the hill and chat about it.
<br /><br />
<i>The three new friends walk off together, up a winding grassy ridge, towards the peak.</i>
<br /><br />
<i>Tortoise: </i>Where were we?
<br /><br />
<i>Achilles: </i>You were talking about <i>genes, </i>and-
<br /><br />
<i>Tortoise: </i>Ah yes. If we take our whole set of <i>genes</i>, and call it our <i>genotype</i>, we can say that through a series of <i>processes</i>, which we might call <i>development</i>, the genotype leads to <i>us</i>, which we might call our <i>phenotype</i>, which is all of the traits and behaviors that we have. I think what Sewell is getting across with that diagram is that <i>combinations </i>of traits at the <i>genotype </i>level create the <i>phenotype</i>, or maybe that the <i>combinations </i>of traits at the <i>phenotype</i> level lead to a certain <i>fitness.</i> And you can see from Sewell's diagram that with just a few traits, or a few genes, the number of combinations starts growing really quickly.
<br /><br />

<figure style="width:432px">
    <img class="figure" src="../images/fontana2003.png" alt="(Fontana 2003: http://tuvalu.santafe.edu/~walter/Papers/top.pdf )" width="432px" height="143px">
    <figcaption>Fontana 2003: http://tuvalu.santafe.edu/~walter/Papers/top.pdf</figcaption>
</figure>

<br /><br />
<i>Crab: </i>And our genes are in our DNA right? I think I heard that when I was reading some newspaper article in the future -
<br /><br />
<i>Tortoise: </i>Yes precisely! And if a change in our DNA, a <i>mutation</i>, produces a <i>phenotype</i> that improves the <i>fitness </i>or success of the individual that has that gene, <i>Natural Selection </i>will favor that gene, and it will spread through the whole population over time. In this way, our genotype and phenotype changes over time, to produce animals as different as reptiles, crustaceans, and primates.
<br /><br />
<i>Crab: </i>Well that feels a bit over simplified to me, there must be other forces than Natural Selection affecting the changes in our genes - Mr. Wright talked about the notion of drifting gene frequencies, in which a mutations spread throughout a population or species just by random chance.
<br /><br />
<i>Achilles: </i>Well that's certainly possible, but is it really common enough to worry about much? Does it really have enough of an effect to matter next to the massive force of Natural Selection? I was sitting next to a fellow at the talk named Donald, or Rob, or Reginald or something, and he was getting pretty upset with how much emphasis old Sewell was putting on the importance of drift and random chance.
<br /><br />
<i>Crab: </i>Oh yes, I think I saw him go up and talk to Mr. Wright after the talk. He seemed like an awfully sharp fellow. I don't see how he can belittle the importance of drift though - how else would we be able to explore the vast, rugged landscape of possible traits?
<br /><br />
<i>Achilles: </i>Well I'm not so sure the landscape is rugged at all. What's-his-name mentioned something about ridges, but it seems like it would be tough to have ridges connecting all the possibilities...
<br /><br />
<i>Flash!... Crashooom! Thunder echoes around Mt. Fuji after a large lightning bolt comes down on the other side of Lake Yamanaka. The sky overhead remains blue, but all around the mountain the heavens open up and rain begins to pour down. The three pause a moment to take in the sight, and then continue up the ridge.</i>
<br /><br />
<i>Tortoise: </i>Whew! What a sight! I think we are in for a bit colder weather now. I have an extra scarf in my bag - would either of you like it? No? I suppose your faces are already fairly well covered by your beards - especially you Mr. Crab - and I do say, that is a fine beard! Achilles, why don't you grow yours longer?
<br /><br />
<i>Achilles: </i>Well I've tried, but it's no use. Before I started growing facial hair, back when I was 8, I looked worse, but then one day it suddenly started growing. For the next ten days, it improved. But then, on the morning of the 11<sup>th</sup> day, my beard looked worse than on the tenth. So I shaved it. Since then every time is the same - steady improvement through the 10<sup>th</sup> day and then a turn for the worst, so I shave again. This 10-day beard is the best I'll ever have.
<br /><br />
<i>The Tortoise has taken a small notebook out of her bag and is scribbling furiously in it.</i>
<br /><br />
<i>Crab: </i>Oh, oh, well that's just unimaginative nonsense! You have to explore what it might look like more than one day into the future! When I first started growing my beard, I looked steadily worse for a week, worse all the time. So I would usually shave after 1 day, cut it all off and be done with it. But I was very busy a few weeks ago, big goings-on at work, very busy, and not paying as much attention to my looks, and I let it go - and to my great delight after 7 days my beard took a turn for the better!
<br /><br />
<i>Achilles: </i>And it's been getting better ever since?
<br /><br />
<i>Crab: </i>Well not exactly, no not exactly. On the 11<sup>th</sup> day it was worse than the 10<sup>th</sup>.
<br /><br />
<i>Achilles: </i>Of course.
<br /><br />
<i>Crab: </i>And I wasn't busy anymore, but I decided, based on the unexpected upturn after 7 days, to explore the possibilities a bit more. It was difficult, after 14 days my face was a sorry sight, a sorry sight indeed. But on day 15 I reached a pivotal moment, a turning point you see, and my beard began to improve again.
<br /><br />

<figure style="width:567px">
<img class="figure" src="../images/tortoisenote.png" alt="The Tortoise's Sketch" width="567" height="331" />
    <figcaption>The Tortoise's Sketch</figcaption>
</figure>

<br /><br />
<i>Achilles: </i>And what day is it now?
<br /><br />
<i>Crab: </i>Well this is day 47, there have been some more ups and downs, but more ups I think overall.
<br /><br />
<i>At this point, the author of this dialogue again tried removing the first mention of Mr. Crab's beard. This time the removal made the dialogue confusing - the later references to the beard, of which there are simply too many to remove, came out of nowhere. As such, the author was forced to go back and include the original comment.  While the author was considering this change, the three travelers crested a small peak on the ridge and looked across a small valley towards the ultimate peak of Mt. Fuji.</i>
<br /><br />
<i>Achilles: </i>This isn't right! The ridge was supposed to take us directly to the peak. Last time I climbed here I didn't have to descend at all, this route was uphill all the way to the top.
<br /><br />
<i>Crab: </i>Perhaps the landscape has changed. I suppose we'll just have to walk through the little valley to reach the peak.
<br /><br />
<i>Achilles: </i>I will not. I didn't come up here just to start walking down again. Either there is a way to the peak in which we don't have to go down, or we should just stay here.
<br /><br />
<i>Tortoise: </i>It is a nice view isn't it? Maybe we should rest here for a bit and talk. I wouldn't mind getting to know our new friend a bit more - say, Mr. Crab, what do you do for a living?
<br /><br />
<i>Crab: </i>Well I'm actually having a bit of a midlife crisis right now, I'm really all out of sorts, and thinking of changing career paths. But my current gig, I'm a little embarrassed to say, isâ€¦well, well, well, I run the world's largest crab fishing company.
<br /><br />
<i>Tortoise: </i>What!?! Don't you feel traitorous to your species?
<br /><br />
<i>Crab: </i>Well it's not my species, not at all. You see, I'm a land crab, <i>Discoplax hirtipes</i>, and I'm in the business of catching <i>Chionoecetes</i>, which is snow crab, you know, no-crab-like-snow-crab, right? But yes, yes, it's <i>Chionoecetes</i> so it's not even my genus. Still, we're in the same order and it does feel a bit wrong at times.
<br /><br />
<i>Achilles: </i>And how did you get into the morbid business in the first place?
<br /><br />
<i>Crab: </i>Oh it must have been 5 years ago. I was alone out there and couldn't find food to eat, a place to sleep, or, or, friends with which to discuss logic, music, and the like. Luckily I stumbled upon another land crab working for the company, and he got me an entry-level job, got me started right away. The next 4 years I worked my way up, up to the top, and now, now I'm the CEO. And year after year I was wealthier and happier, wealthier and happier every year. But the last year I've just felt like there is nowhere to go, I'm just sitting in place, sitting twiddling around and not, not changing anything, not getting any happier. I'm not quite sure the crab fishing business is what I'm meant to do...
<br /><br />
<i>Tortoise: </i>Well what <i>would </i>you like to do then?
<br /><br />
<i>Crab: </i>Well I didn't know, I hadn't known, for the longest time, which is why I haven't quit my job. But I think I know what I <i>would </i>like now - it actually came to me after listening to Mr. Wright's talk - I'd like to be an evolutionary biologist and a professor!
<br /><br />
<i>Achilles:</i> Wow, that's quite an aspiration. But Mr. Crab, I think you're being a bit unreasonable. Look, you can't just quit your crab-fishing job today and be an evolutionary biologist tomorrow. You'll have to go back to school, take some community college courses, get a degree, take the GRE, contact schools, send in applications, and then you have to spend 5 more years in school getting a PhD! All the while you'll be getting poorer, you will lose the prestige of being a CEO, and you'll be slaving away long hours at schoolwork. I empathize with your predicament, but it seems like you'd be better off sticking with your current job.
<br /><br />
<i>Crab: </i>Well that's why I am in crisis right now, trying to decide if I am going to jump down off my position as CEO, scramble around a bit, and begin climbing the ladder of academia. It seems there's not a way to get to where I want to go without sacrificing everything. But I've <i>imagined </i>myself as a professor of evolutionary biology for quite some time now, and I'm sure I will like it, sure I will love it in fact. So I'm decided I think, I'm going to quit my job and venture out into that dark unknown.
<br /><br />
<i>Achilles: </i>Mr. Crab, I really think that - oh dear!
<br /><br />
<i>In the heat of their conversation, the three failed to notice that the rain around the mountain had a profound effect. The massive downpour combined Lake Kawaguchi, Yamanaka, Sai, Motosu, and Shoji into one massive Lake Fuji, which rose until at this moment Crab, Achilles, and Tortoise found themselves just a foot above the water.</i>
<br /><br />
<i>Achilles: </i>Oh no, oh dear. What's happened now?
<br /><br />
<i>Crab: </i>The water is rising my friend. We had better cut our losses now and walk through the valley to the peak. It's the only place we'll stay out of the flood.
<br /><br />
<i>Tortoise: </i>But the valley is underwater now, and Achilles and I are unusually dense animals, so we'd have to walk through it.
<br /><br />
<i>Achilles: </i>Perhaps I could jump it (<i>lining up a running start, then stopping just short)</i>. No, it's too far. I guess I'll just wait on this ridge for the water to overtake me.
<br /><br />
<i>Crab: </i>Nonsense! Follow me.
<br /><br />
<i>And with that the crab scuttled into the water. Achilles began to follow him but upon taking one step downhill, he returned to the ridge.</i>
<br /><br />
<i>Achilles: </i>No, I tried, I tried. I refuse to walk downhill!
<br /><br />
<i>But the crab couldn't hear Achilles, he was already making his way across the underwater valley and... up to the peak on the other side.</i>
<br /><br />
<i>Tortoise: </i>Wow, that actually worked. I don't know if I'd be able to survive going through that valley though -
<br /><br />
<i>Achilles: </i>So you'll stay here with me? Oh dear, oh dear me.
<br /><br />
<i>Tortoise: </i>Well don't lose your cool, my friend. Hold on a second, there might be another way...
<br /><br />
<i>Ms. Tortoise removes her map and begins to look at it intently. Achilles looks over her shoulder, puzzled. It is unlike any map he's ever seen before, with lines going in every direction ... and then some. Tortoise pulls a pair of goggles from her bag, puts them on, and starts wandering along the thin ridges of land exposed above the water, looking at her map.</i>
<br /><br />
<i>Tortoise: </i>OK, it should be right about here, yes just a bit to the left here.
<br /><br />
<i>Achilles: </i>What in GOD's name are you looking for? There's nothing there. And why are you wearing goggles? Ah, we're stuck on this ridge and we're doomed. This is it.
<br /><br />
<i>Tortoise: </i>Calm down. Now I have to tell you something. I've known it for a little while but I haven't told you because ... well I wasn't sure how you'd take it.
<br /><br />
<i>Achilles: </i>Spit it out, my feet are wet!
<br /><br />
<i>Tortoise: </i>Well... ok, so there are more than 3 dimensions that we can move through as we wish. Here, put on these goggles.
<br /><br />
<i>Tortoise removes another pair of goggles from her bag and gives them to Achilles. He puts them on and finds himself in an unfamiliar world, a world with more than 3 spatial dimensions. The landscape before him multiplies in complexity. Ms. Tortoise grabs Achilles hand and they walk along a flat path through the surreal space. They are still on same ridge, but they've moved too. Achilles looks across and sees that a continuous ridge now connects their position to the peak. They run together up to the peak, moving in a familiar way again, but still in the strange space.</i>
<br /><br />
<i>Achilles: </i>Oh excellent, we're safe.
<br /><br />
<i>Achilles pulls off his goggles and finds himself in a world he is accustomed to. The ridge below him leads steadily down to where they stood before. But off in the distance behind it Achilles sees a peak that is clearly higher than the one he is standing on.</i>
<br /><br />
<i>Achilles: </i>And what is that peak? It must be Mt. Fuji, it's the highest point in the area. But then where are we?
<br /><br />
<i>Tortoise (removing her goggles as well): </i>We are on Mt. Fuji, though not the one you're familiar with I suppose. Well, see, this is hard to explain. The mountain over there is indeed higher, but it's not named because there's no one to name it. In fact, we're the only animals who have ever seen that peak.
<br /><br />
<i>Achilles: </i>What? Now you've completely lost me. And if this is Mt. Fuji then - wait, where is Mr. Crab?
<br /><br />
<i>Tortoise: </i>He's back where we came from.
<br /><br />
<i>Achilles: </i>But he was here, on the peak.
<br /><br />
<i>Tortoise: </i>Yes, he's here, but he's also back where we came from. Oh, this is what I was afraid of. Put your goggles on again.
<br /><br />
<i>Achilles: </i>But -
<br /><br />
<i>Tortoise: </i>Trust me.
<br /><br />
<i>The two put back on their goggles, and Tortoise leads Achilles along another flat pathway. They stay on the peak, but walk until -
</i>
<br /><br />
<i>Achilles: </i>Mr. Crab! You are a sight for sore eyes. <i>(Ripping off his goggles). </i>Oh and this is the proper world. What a trip that was.
<br /><br />
<i>Crab:   </i>Wow, you just appeared, appeared out of thin air! You'll have to tell me how you did that little trick sometime!
<br /><br />
<i>Achilles: </i>And that other pesky peak isn't here. Excellent! We've done it! We're on the highest possible point, with the optimum view. I feel so relieved.
<br /><br />

<img class="figure" src="../images/hillsketch-01.png" alt="hillsketch-01" width="640" height="226" />

<br /><br />
<i>Tortoise: </i> Well I'm glad you've calmed down. Mr. Crab, I was thinking while we were walking up here about the possible ways you could become an evolutionary biologist, and I have an idea. I know of a nearby night school where you could take undergraduate courses and study for the GRE, and I have a friend who is a professor studying the evolution of <i>Chionoecetes. </i>You could keep your job and slowly transition into being an evolutionary biologist, first with night school classes and then by studying the animal you catch! That way you wouldn't have to sacrifice anything and your happiness could steadily improve!
<br /><br />
<i>Achilles: </i>Well that sounds like an excellent solution!
<br /><br />
<i>Crab: </i>Agreed. What a creative way to circumvent the nasty intermediate stage I was facing!
<br /><br />
<i>Achilles: </i>Speaking of nasty intermediates, how do you suppose we'll ever get off of this mountain with the lake around us to some part of the world that isn't flooded?
<br /><br />
<i>Tortoise: </i>(<i>Pulling out her map again</i>) I think I can find a way.
<br /><br />
<i>Crab: </i>(<i>to Achilles</i>)And what exactly does she mean by that?
<br /><br />
<i>Achilles: (whispering rather loudly) </i>I think there are actually four spatial dimensions!
<br /><br />
<i>Tortoise: </i>(<i>to herself) </i>Sure, there are just four...
